#float
a=13.55
print(type(a),a)

#complex
b=5j
print(type(b),b)

#list
c=["red","blue","green"]
print(type(c),c)

#tuple
d=("one","two","three")
print(type(d),d)

#range
e=range(10)
print(type(e),e)

#dictionary

x={"Firstname":"Adarsh","Middlename":"Vishnu","Lastname":"Tayde"}
print(type(x),x)

#Set

x={"red","green","blue"}
print(type(x),x)

